package p03.lecture.p2binary.p2compare;

public class A01compare {
	public static void main(String[] args) {
		// ==(같다), != (다르다)
		// < (작다) , <= (작거나 같다)
		// > (크다), >=(크거나 같다)
		
		int a = 3;
		int b = 8;
		
		System.out.println(a == b);
		System.out.println(a != b);
		System.out.println(a < b);
		System.out.println(a <= b);
		System.out.println(a > b);
		System.out.println(a >= b);
		
	
	}
}
