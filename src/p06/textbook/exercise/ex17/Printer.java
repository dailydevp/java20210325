package p06.textbook.exercise.ex17;

public class Printer {
	 static void println (int i) {
		System.out.println(i);
	}
	
	static void println (boolean i) {
		System.out.println(i);
	}
	
	static void println (double i) {
		System.out.println(i);
	}
	
	static void println (String i) {
		System.out.println(i);
	}
}
