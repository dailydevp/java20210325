package p06.textbook.exercise.ex17;

public class Printer {
	 void println (int i) {
		System.out.println(i);
	}
	
	public void println (boolean i) {
		System.out.println(i);
	}
	
	public void println (double i) {
		System.out.println(i);
	}
	
	public void println (String i) {
		System.out.println(i);
	}
}
